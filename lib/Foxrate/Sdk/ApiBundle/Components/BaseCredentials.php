<?php

class Foxrate_Sdk_ApiBundle_Components_BaseCredentials
{
    protected $errors;

    /**
     * @param mixed $errors
     */
    public function setErrors($errors)
    {
        $this->errors = $errors;
    }

    /**
     * @return mixed
     */
    public function getErrors()
    {
        return $this->errors;
    }


}